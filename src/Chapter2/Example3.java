package Chapter2;
// characters can be handled an integer value 
public class Example3 {

	public static void main(String[] args) {
		char ch;
		ch = 'x';
		System.out.println("ch contains " + ch);

		ch++; // char can be incremented 
		System.out.println("ch now is " + ch);
		
		ch = 90; // char can contains an integer 
		System.out.println("ch now is " + ch);
	}

}
